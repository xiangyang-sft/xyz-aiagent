"""
综合对比：Tool Calling 的演进和最佳实践总结
"""

COMPARISON = """
╔══════════════════════════════════════════════════════════════╗
║                  工具调用 (Tool Calling) 总结              ║
╚══════════════════════════════════════════════════════════════╝

┌─ 工具调用的本质 ──────────────────────────────────────────┐
│                                                           │
│  「LLM 是大脑，工具是手和眼睛」                              │
│                                                           │
│  LLM 自己不能：                                            │
│    1. 获取实时信息（知识截止）                                │
│    2. 执行精确计算（数学不可靠）                              │
│    3. 操作外部系统（文本输出无副作用）                        │
│    4. 拥有长期记忆（上下文窗口有限）                          │
│                                                           │
│  Function Calling = LLM 决定「调用什么」≠ 执行              │
│  开发者执行函数，把结果送回 LLM                              │
│                                                           │
└───────────────────────────────────────────────────────────┘

┌─ 核心 API 参数 ──────────────────────────────────────────┐
│                                                           │
│  tools: [...]      → 工具描述列表（name + schema）          │
│  tool_choice:      → 控制工具调用行为                       │
│    ├ auto          → LLM 自行决定（默认，最灵活）            │
│    ├ required      → 必须调用一个工具（测试/路由场景）        │
│    ├ none          → 禁止调用工具（纯对话场景）              │
│    └ {type,function:{name}} → 强制调用指定工具              │
│                                                           │
│  返回格式：                                                │
│    message.tool_calls → [{id, type, function: {name, arguments}}]
│                                                           │
└───────────────────────────────────────────────────────────┘

┌─ 工具设计最佳实践 ──────────────────────────────────────┐
│                                                           │
│  1. 命名规范                                              │
│     好的: get_current_weather, search_users               │
│     差的: func1, process, do_stuff                         │
│                                                           │
│  2. 描述要精准                                             │
│     差的: "获取天气"                                       │
│     好的: "获取指定城市的实时天气数据，包括温度、湿度、风力"    │
│                                                           │
│  3. 参数要完整                                             │
│     - 类型标注（string, number, object, array, enum）       │
│     - required 明确哪些是必填                              │
│     - description 让 LLM 知道怎么填                        │
│     - enum 约束可选值                                      │
│                                                           │
│  4. 单一职责                                              │
│     每个工具只做一件事，且做好                              │
│     工具粒度适中：太粗 LLM 难用，太细列表太长               │
│                                                           │
│  5. 错误处理                                              │
│     - 统一错误格式 {status, data/error}                    │
│     - 让 LLM 能理解错误并决定下一步                         │
│     - 不要在工具里抛异常，要返回错误消息                     │
│                                                           │
└───────────────────────────────────────────────────────────┘

┌─ 工具调用演进路线 ──────────────────────────────────────┐
│                                                           │
│  Stage 1: 手动解析 ReAct                                    │
│    正则从文本提取 Action + Action Input                    │
│    优点：兼容所有模型                                      │
│    缺点：脆弱、格式要求严                                  │
│                                                           │
│  Stage 2: 原生 Function Calling                            │
│    API 原生支持 tools 参数，结构化返回                      │
│    优点：稳定、支持并行调用                                │
│    缺点：依赖模型能力                                      │
│                                                           │
│  Stage 3: MCP 标准化协议                                   │
│    标准化工具发现、调用、错误处理                          │
│    优点：一次实现到处用，跨框架兼容                        │
│    优点：支持动态发现工具                                  │
│                                                           │
│  Stage 4: Agent 框架封装                                   │
│    LangChain Tool / CrewAI Tool / AutoGen Tool            │
│    优点：开箱即用，支持缓存、重试、组合                    │
│                                                           │
└───────────────────────────────────────────────────────────┘

┌─ 面试题 ──────────────────────────────────────────────┐
│                                                           │
│  Q1: Function Calling 和 纯文本 ReAct 有什么区别？        │
│  A1: FC 是结构化 API（返回 JSON），稳定支持并行和          │
│      tool_choice；ReAct 是文本正则提取，通用但脆弱。       │
│      ReAct 是思想，FC 是其工程落地的最佳实践。              │
│                                                           │
│  Q2: tool_choice 四种模式的使用场景？                      │
│  A2: auto = 通用；required = 路由/测试；                  │
│      none = 纯对话；强制指定 = 确定走某个工具。            │
│      坑：required 可能选错工具，强制指定完全由开发者控制。  │
│                                                           │
│  Q3: 并行工具调用有限制吗？                               │
│  A3: 工具间不能有依赖；模型需支持（GPT-4+）；              │
│      部分模型有数量上限（5-10）；超过 5 个准确率可能下降。  │
│                                                           │
│  Q4: 工具描述如何影响准确率？                             │
│  A4: 影响巨大。描述从 1 句到 3 句提升 15-20% 准确率，      │
│      加 enum 减少 30% 参数错误。好的描述 = 高准确率。      │
│                                                           │
│  Q5: 工具应该抛异常还是返回错误消息？                      │
│  A5: 返回结构化错误消息！LLM 需要看到错误决定下一步，      │
│      抛异常会导致流程中断。统一格式 {status, data/error}。  │
│                                                           │
│  Q6: 工具粒度怎么设计？                                   │
│  A6: 粗粒度省 token 但参数复杂；细粒度精准但列表长；       │
│      按领域分组最推荐。经验：每个工具 3-5 参数，总量 < 30。 │
│                                                           │
│  Q7: MCP 和普通 Function Calling 什么关系？                │
│  A7: MCP 是 FC 的上层标准化协议。                         │
│      FC 是 HTTP，MCP 是 REST API。MCP 解决碎片化、         │
│      不可移植、动态发现三个问题。                           │
│                                                           │
│  Q8: 如何避免工具调用死循环？                             │
│  A8: 1.最大迭代次数；2.Token 预算；3.结果去重；            │
│      4.is_complete flag；5.分阶段设计。最常用是迭代上限。   │
│                                                           │
│  Q9: 多轮对话中工具结果如何管理上下文？                    │
│  A9: 不要保留全部！只保留最近成功结果；做摘要压缩；         │
│      按时间衰减；重要结果存入结构化存储而非上下文。         │
│                                                           │
│  Q10: 工具调用质量怎么测试和评估？                         │
│  A10: Unit Test + E2E。关键指标：工具选择准确率 >95%，     │
│      参数生成准确率 >90%，响应速度 <3s，无效调用率 <5%，   │
│      重试率 <10%。用 langfuse/helicone/langsmith 监控。    │
│                                                           │
└───────────────────────────────────────────────────────────┘
"""

