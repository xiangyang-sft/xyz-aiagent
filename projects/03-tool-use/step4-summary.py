"""
综合对比：Tool Calling 的演进和最佳实践总结
"""

COMPARISON = """
╔══════════════════════════════════════════════════════════════╗
║                  工具调用 (Tool Calling) 总结              ║
╚══════════════════════════════════════════════════════════════╝

┌─ 工具调用的本质 ──────────────────────────────────────────┐
│                                                           │
│  「LLM 是大脑，工具是手和眼睛」                              │
│                                                           │
│  LLM 自己不能：                                            │
│    1. 获取实时信息（知识截止）                                │
│    2. 执行精确计算（数学不可靠）                              │
│    3. 操作外部系统（文本输出无副作用）                        │
│    4. 拥有长期记忆（上下文窗口有限）                          │
│                                                           │
│  Function Calling = LLM 决定「调用什么」≠ 执行              │
│  开发者执行函数，把结果送回 LLM                              │
│                                                           │
└───────────────────────────────────────────────────────────┘

┌─ 核心 API 参数 ──────────────────────────────────────────┐
│                                                           │
│  tools: [...]      → 工具描述列表（name + schema）          │
│  tool_choice:      → 控制工具调用行为                       │
│    ├ auto          → LLM 自行决定（默认，最灵活）            │
│    ├ required      → 必须调用一个工具（测试/路由场景）        │
│    ├ none          → 禁止调用工具（纯对话场景）              │
│    └ {type,function:{name}} → 强制调用指定工具              │
│                                                           │
│  返回格式：                                                │
│    message.tool_calls → [{id, type, function: {name, arguments}}]
│                                                           │
└───────────────────────────────────────────────────────────┘

┌─ 工具设计最佳实践 ──────────────────────────────────────┐
│                                                           │
│  1. 命名规范                                              │
│     好的: get_current_weather, search_users               │
│     差的: func1, process, do_stuff                         │
│                                                           │
│  2. 描述要精准                                             │
│     差的: "获取天气"                                       │
│     好的: "获取指定城市的实时天气数据，包括温度、湿度、风力"    │
│                                                           │
│  3. 参数要完整                                             │
│     - 类型标注（string, number, object, array, enum）       │
│     - required 明确哪些是必填                              │
│     - description 让 LLM 知道怎么填                        │
│     - enum 约束可选值                                      │
│                                                           │
│  4. 单一职责                                              │
│     每个工具只做一件事，且做好                              │
│     工具粒度适中：太粗 LLM 难用，太细列表太长               │
│                                                           │
│  5. 错误处理                                              │
│     - 统一错误格式 {status, data/error}                    │
│     - 让 LLM 能理解错误并决定下一步                         │
│     - 不要在工具里抛异常，要返回错误消息                     │
│                                                           │
└───────────────────────────────────────────────────────────┘

┌─ 工具调用演进路线 ──────────────────────────────────────┐
│                                                           │
│  Stage 1: 手动解析 ReAct                                    │
│    正则从文本提取 Action + Action Input                    │
│    优点：兼容所有模型                                      │
│    缺点：脆弱、格式要求严                                  │
│                                                           │
│  Stage 2: 原生 Function Calling                            │
│    API 原生支持 tools 参数，结构化返回                      │
│    优点：稳定、支持并行调用                                │
│    缺点：依赖模型能力                                      │
│                                                           │
│  Stage 3: MCP 标准化协议                                   │
│    标准化工具发现、调用、错误处理                          │
│    优点：一次实现到处用，跨框架兼容                        │
│    优点：支持动态发现工具                                  │
│                                                           │
│  Stage 4: Agent 框架封装                                   │
│    LangChain Tool / CrewAI Tool / AutoGen Tool            │
│    优点：开箱即用，支持缓存、重试、组合                    │
│                                                           │
└───────────────────────────────────────────────────────────┘

┌─ 面试题 ──────────────────────────────────────────────┐
│                                                           │
│  Q1: Function Calling 和 纯文本 ReAct 有什么区别？        │
│  A1: FC 是结构化 API（原生支持，返回 JSON），              │
│      ReAct 是从文本正则提取（通用但不稳定）。               │
│      FC 支持并行调用、tool_choice 控制。                  │
│                                                           │
│  Q2: tool_choice='required' 和 'auto' 各什么场景用？      │
│  A2: auto 通用场景；required 用于路由/分类/测试；          │
│      强制指定用于确定需要某个工具的场景。                   │
│                                                           │
│  Q3: 多个工具返回结果怎么关联？                            │
│  A3: 每个 tool_call 有一个唯一 id，tool 消息用            │
│      tool_call_id 关联到对应的调用。并行调用时框架会       │
│      自动组织 messages 列表。                              │
│                                                           │
│  Q4: 工具描述写得好有什么影响？                            │
│  A4: 直接影响 LLM 调用准确率。好描述 = 高准确率。          │
│      关键是说明「什么时候用」「参数怎么填」「返回什么」。    │
│                                                           │
│  Q5: MCP 解决了什么问题？                                 │
│  A5: 标准化。之前每个框架自己定义工具格式，MCP 统一了      │
│      发现、调用、错误处理的协议。一次实现，到处可用。       │
│                                                           │
└───────────────────────────────────────────────────────────┘
"""

